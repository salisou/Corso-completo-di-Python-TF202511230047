"""
    Tipi Dati in python
    int = intero
    str = string
    float = decimali, float (numeri con virgole)
"""

# <class 'str'>
nome = 'Marco'


# <class 'int'>
eta = 65

# <class 'float'>
stipendio = 1.800

# <class 'bool'>
lavoratore = True

# stampo il tipo della variabile "nome"
print(type(nome))
print(type(eta))
print(type(stipendio))
print(type(lavoratore))